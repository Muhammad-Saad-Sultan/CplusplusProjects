#include <iostream>
using namespace std;
int main()
{
    int value1;
    char value2;
    float value3;
    double value4;

    cout << "The size of int is: " << (sizeof(int)) << endl;
    cout << "The size of char is: " << (sizeof(char)) << endl;
    cout << "The size of float is: " << (sizeof(float)) << endl;
    cout << "The size of double is: " << (sizeof(double)) << endl;

return 0;
}